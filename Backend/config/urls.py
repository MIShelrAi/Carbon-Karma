from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static
from rest_framework_simplejwt.views import TokenObtainPairView, TokenRefreshView

urlpatterns = [
    path('admin/', admin.site.urls),

    path('api/token/', TokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),

    path('api/users/', include('apps.users.urls')),
    path('api/tracking/', include('apps.tracking.urls')),
    path('api/gamification/', include('apps.gamification.urls')),
    path('api/challenges/', include('apps.challenges.urls')),
    path('api/leaderboard/', include('apps.leaderboard.urls')),
    path('api/rewards/', include('apps.rewards.urls')),
    path('api/emissions/', include('apps.emissions.urls')),
]

if settings.DEBUG:
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)

admin.site.site_header = "Carbon Karma Admin"
admin.site.site_title = "Carbon Karma Admin Portal" 
admin.site.index_title = "Welcome to Carbon Karma Admin Portal"